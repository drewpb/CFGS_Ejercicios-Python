def comparador(num1, num2):
    if (num1<0 or num2<0):
        print("Lo siento, no se admiten números negativos.")
    elif (num2==0 or num1==0):
        print("Lo siento, no se admiten números nulos.")
    else:
        if (num1>=num2):
            resto = num1%num2
            if (resto == 0):
                print(f"{num1} es múltiplo de {num2}.")
            else:
                print(f"{num1} no es múltiplo de {num2}.")
        elif (num2>num1):
            resto = num2%num1
            if (resto == 0):
                print(f"{num2} es múltiplo de {num1}.")
            else:
                print(f"{num2} no es múltiplo de {num1}.")
    return


#////////////////////////////////////////////////////////////////////////////////////
