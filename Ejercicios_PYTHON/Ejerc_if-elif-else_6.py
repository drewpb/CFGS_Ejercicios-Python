def comprobador(año):
    proba4 = año % 4
    proba100 = año % 100
    proba400 = año % 400
    if (año == 0):
        print("Lo siento, no se admite el año cero.")
    else:
        if proba4 == 0 and (proba100 != 0 or proba400 == 0):
            print(f"El año {año} SI es bisiesto.")
        else: 
            print(f"El año {año} NO es bisiesto.")

