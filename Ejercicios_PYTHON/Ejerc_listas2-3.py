def esPrimo(num):
    if num <= 0:
        print("El número debe ser mayor que cero")
    elif num == 1:
        return True 
    else:
        cant_divisores = 0
        i = 1
        while (i <= num):
            if num % i == 0:
                cant_divisores += 1
            i += 1
        if cant_divisores == 2:
            return True
        else:
            return False

